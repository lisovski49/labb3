# Открываем файл для чтения
with open('subjects.rtf', 'r') as f:
    # Создаем пустой словарь для хранения количества занятий по предметам
    subjects_dict = {}
    # Читаем строки из файла
    for line in f:
        # Разбиваем строку на название предмета и список занятий
        subject, lessons = line.split(':')
        # Создаем переменную для хранения общего количества занятий по предмету
        total_lessons = 0
        # Разбиваем список занятий на отдельные элементы
        for lesson in lessons.split():
            # Извлекаем количество занятий из элемента и преобразуем его в число
            num_lessons = int(lesson.split('(')[0])
            # Добавляем количество занятий к общему количеству занятий по предмету
            total_lessons += num_lessons
        # Добавляем название предмета и общее количество занятий по нему в словарь
        subjects_dict[subject] = total_lessons

    # Выводим словарь на экран
    print(subjects_dict)