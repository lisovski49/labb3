# Открываем файл для чтения
with open('employees.rtf', 'r') as f:
    # Создаем список для хранения фамилий сотрудников с окладом более 10 тысяч
    high_salary_employees = []
    # Создаем переменную для хранения суммы окладов всех сотрудников
    total_salary = 0
    # Создаем переменную для хранения количества сотрудников
    num_employees = 0
    # Читаем строки из файла
    for line in f:
        # Разбиваем строку на фамилию и оклад
        name, salary = line.split()
        # Преобразуем оклад в число с плавающей точкой
        salary = float(salary)
        # Добавляем оклад к общей сумме окладов
        total_salary += salary
        # Увеличиваем счетчик сотрудников
        num_employees += 1
        # Если оклад сотрудника больше 10000, добавляем его фамилию в список
        if salary > 10000:
            high_salary_employees.append(name)

    # Выводим фамилии сотрудников с окладом более 10 тысяч
    print("Сотрудники с окладом более 10 тысяч:", ', '.join(high_salary_employees))
    # Вычисляем среднюю величину дохода сотрудников
    avg_salary = total_salary / num_employees
    # Выводим среднюю величину дохода сотрудников
    print("Средняя величина дохода сотрудников:", avg_salary)